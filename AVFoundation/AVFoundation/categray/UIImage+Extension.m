//scaleaspect的算法
+ (UIImage *)imageWithImageSimple:(UIImage *)image withImageViewSize:(CGSize)size{
    
    CGSize sizeForImage = image.size;
    
    CGFloat scale = sizeForImage.width / sizeForImage.height;
    
    CGSize newSize;
    if (size.width * scale < size.height) {
        newSize = CGSizeMake(size.height / scale, size.height);
    }else {
        newSize = CGSizeMake(size.width, size.width * scale);
    }
    
    // Create a graphics image context
    UIGraphicsBeginImageContext(newSize);
    // Tell the old image to draw in this newcontext, with the desired
    // new size
    [image drawInRect:CGRectMake(0,0,newSize.width,newSize.height)];
    // Get the new image from the context
    UIImage* newImage = UIGraphicsGetImageFromCurrentImageContext();
    // End the context
    UIGraphicsEndImageContext();
    // Return the new image.
    return newImage;
}

